package by.it_academy.jd2.Mk_JD2_92_22.garbage;

public class Employee {
    private long id;
    private String name;
    private long job;
    private long dep;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getJob() {
        return job;
    }

    public void setJob(long job) {
        this.job = job;
    }

    public long getDep() {
        return dep;
    }

    public void setDep(long dep) {
        this.dep = dep;
    }
}
